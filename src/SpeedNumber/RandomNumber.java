public void timeDemo(){
    timer = new Timer(10, new ActionListener() {
        @Override
        public void actionPerformed(ActionEvent ae) {
            count0++;
               
            if(count0 == 100){
                count0 = 0;
                count1++;
                playSound("woodclock.wav");
                if(count1 == 60){
                    timer.stop();
                    close();
                    frecall.setVisible(true);
                }
                tf0.setText(String.valueOf(count1));
            }
            tf2.setText(String.valueOf(count0));
        }
    });
}
