    private void tftypeKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tftypeKeyTyped
      playSound("type.wav");
        String str = tftype.getText()+evt.getKeyChar();
      int index = str.indexOf(evt.getKeyChar());
      
      if(str3.charAt(index)!= evt.getKeyChar()){
          playSound("Eee.wav");
          try {
              Thread.sleep(3000);
          } catch (InterruptedException ex) {
              Logger.getLogger(RandomNumber.class.getName()).log(Level.SEVERE, null, ex);
          }
            frecall.setVisible(false);
         timeDemo1();
        timer.start();
      }
      else{
          if(str.length() == 18){
            playSound("vo_tay.wav");
              frecall.setVisible(false);
              dlgWin.setVisible(true);
              lbWin.setText(str3);
              lbCo.setText("18/18");
              lbTi.setText( String.valueOf(count1)+":"+String.valueOf(count0));
          }
      }
    }//GEN-LAST:event_tftypeKeyTyped

